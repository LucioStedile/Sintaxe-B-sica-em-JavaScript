//tipos primitivos
//booleanos

var vOuF = false;
console.log(typeof(vOuF))
console.log(vOuF);

//number
var numeroQualquer = 1;
console.log(typeof(numeroQualquer));
console.log(numeroQualquer);

//String
var nome = 'Lúcio Stedile';
console.log(typeof(nome));
console.log(nome);

//var é de uso local ou global
// se não tiver valor é tratado como nulo
var variavel;
console.log(typeof(variavel));
console.log(variavel);

//let é de uso local
let variavel2 = 'Lúcio'
console.log(typeof(variavel2));
console.log(variavel2);

//constante é de uso local não pode ser alterado e é obrigatório ter valor
const nome2 = 'Lúcio';
console.log(typeof(nome2));
console.log(nome2);